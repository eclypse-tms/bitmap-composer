package io.eclypse.bitmapcomposer.ui.theme

/**
 * [Emphasis] is used to determine what part of the content to emphasize by changing the color,
 * text size or by using bolder fonts. This is typically used in ListItems allows the developer
 * to highlight either the leading or trailing content.
 */
enum class Emphasis {
    LeadingContent,
    TrailingContent,
}